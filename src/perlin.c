double			grad(int hash, double x, double y, double z)
{
	double		u;
	double		v;
	double		a;
	double		b;

	hash = hash & 15;
	u = hash < 8 ? x : y;
	v = hash == 12 || hash == 14 ? x : z;
	v = hash < 4 ? y : v;
	a = (hash & 1) == 0 ? u : -u;
	b = (hash & 2) == 0 ? v : -v;
	return (a + b);
}

double			perlin_noise(double x, double y, double z)
{
	int			xyz[3];
	double		uvw[3];
	int			aabb[2];
	int			ab[2][2];

	X = (int)x & 255;
	Y = (int)y & 255;
	Z = (int)z & 255;
	x -= (int)x;
	y -= (int)y;
	z -= (int)z;
	uvw[0] = FADE(x);
	uvw[1] = FADE(y);
	uvw[2] = FADE(z);
	A = g_p[X] + Y;
	B = g_p[X + 1] + Y;
	AA = g_p[A] + Z;
	AB = g_p[A + 1] + Z;
	BA = g_p[B] + Z;
	BB = g_p[B + 1] + Z;
	return (LERP(W, LERP(V, LERP(U, grad(g_p[AA], x, y, z), grad(g_p[BA], x - 1,
		y, z)), LERP(U, grad(g_p[AB], x, y - 1, z), grad(g_p[BB], x - 1,
		y - 1, z))), LERP(V, LERP(U, grad(g_p[AA + 1], x, y, z - 1),
		grad(g_p[BA + 1], x - 1, y, z - 1)), LERP(U, grad(g_p[AB + 1],
		x, y - 1, z - 1), grad(g_p[BB + 1], x - 1, y - 1, z - 1)))));
}
